/*
    >   - maior que
    <   - menor que
    >=  - maior ou igual a
    <=  - menor ou igual a
    ==  - igualdade (valor)
    === - igualdade estrita (valor e tipo)
    !=  - diferente (valor)
    !== - diferente estrito (valor e tipo)
*/

console.log(10 > 5);
console.log(10 >= 10);
console.log(5 < 10);
console.log(5 <= 5);
console.log(5 == 5);
console.log(5 == '5');
console.log(5 === '5');
console.log(5 != '5');
console.log(5 !== '5');